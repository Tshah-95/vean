// Tests for the IR → .mlt serializer (`toMlt`). The serializer is a FORMAT
// CONTRACT — its output is read by melt and by Shotcut, and the parser is its
// exact inverse — so it is golden-tested: one representative timeline exercising
// every shape (multi video + audio tracks, head/tail fades, a same-track
// dissolve, a windowed file clip, blanks, gain, a verbatim animation-string
// filter, a field transition) is locked byte-for-byte, and targeted assertions
// pin each AUTHORITATIVE MLT FACT so a regression names which contract it broke.
import { describe, expect, it } from "vitest";
import {
  LANDSCAPE_2997,
  type Timeline,
  VERTICAL,
  audioTrack,
  blank,
  clip,
  colorClip,
  dissolve,
  filter,
  resetIds,
  timeline,
  toMlt,
  transition,
  videoTrack,
} from "../src/index";

// ── The representative timeline (the golden fixture) ──
// Rebuilt from scratch each call (with resetIds) so id assignment is a pure
// function of the IR — the determinism contract.
function fixture(): Timeline {
  resetIds();
  return timeline(
    VERTICAL,
    {
      video: [
        videoTrack(colorClip(45, "black", { fadeIn: 12 }), dissolve(20), colorClip(60, "gold")),
        videoTrack(blank(10), clip("/abs/overlay.mp4", { in: 20, out: 79, fadeOut: 15 })),
      ],
      audio: [
        audioTrack(
          clip("/abs/vo.wav", {
            dur: 125,
            gain: 0.8,
            filters: [filter("brightness", { level: "0=0;14=1" })],
          }),
        ),
      ],
    },
    { transitions: [transition("qtblend", 1, 2, 0, 59, { compositing: 0 })] },
  );
}

